public class TaskTwo {
    public static void main(String[] args) {
        int x = 100;
        System.out.println("Step 1 (x declaration and initialization): " + x);

        x += 6;
        System.out.println("Step 2 (addition): " + x);

        x -= 4;
        System.out.println("Step 3 (subtraction): " + x);

        x *= 4;
        System.out.println("Step 4 (multiplication): " + x);

        x /= 27;
        System.out.println("Step 5 (division): " + x);

        x %= 7;
        System.out.println("Step 6 (modulus division): " + x);
    }
}
